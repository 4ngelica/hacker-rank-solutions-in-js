function plusMinus(arr) {

let positives = arr.filter(num => num >=  0 && num !== 0)
let negatives = arr.filter(num => num <=  0 && num !== 0)
let zeros = arr.filter(num => num === 0 )

let positiveProporcion =  (positives.length/arr.length).toFixed(6)
let negativesProporcion =  (negatives.length/arr.length).toFixed(6)
let zerosProporcion =  (zeros.length/arr.length).toFixed(6)

return console.log(positiveProporcion + "\n" + negativesProporcion + "\n" + zerosProporcion)

}
